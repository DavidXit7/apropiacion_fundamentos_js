let numero = parseInt(prompt("Ingrese un numero: ")) //Se solicita un numero al usuario

numero_duplicado = numero * numero; //Duplicacion del numero digitado

numero_triplicado = numero * numero * numero; //Triplicacion del numero digitado

alert("El doble del numero es de: "+ numero_duplicado) //Se imprime el resultado del numero duplicado
alert("El triple del numero es de: " + numero_triplicado) //Se imprime el resultado del numero triplicado