let base = parseFloat(prompt("Digite el valor de la base: ")); //Se solicita el valor de la base del rectangulo
let altura = parseFloat(prompt("Digite el valor de la altura")); //Se solicita el valor de la altura del rectangulo

area = base * altura; //Se realiza el calculo del area del rectangulo con las variables

alert("El area del rectangulo es de: "+ area); //Se imprime el resultado de la operacion realizada