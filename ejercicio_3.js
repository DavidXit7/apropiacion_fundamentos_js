let nota_1 = parseFloat(prompt("Ingrese el valor de la primer nota: ")); //Se solicita el valor de 3 notas al usuario
let nota_2 = parseFloat(prompt("Ingrese el valor de la segunda nota: "));
let nota_3 = parseFloat(prompt("Ingrese el valor de la tercer nota: "));

promedio = (nota_1 + nota_2 + nota_3) / 3; //Se realiza el calculo del promedio de las notas digitadas

alert("El promedio de las notas es de:  "+promedio); //Se imprime el promedio